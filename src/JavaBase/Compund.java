package src.JavaBase;

public class Compund {

    public static void main(String[] args) {

        int a = 20;

        int b = 21;

        a += 90; //a = a +90

        b += 20; //b = b + 20

        System.out.println(a);

        System.out.println(b);

        int b1 = 999;

        int a1 = 111;

        a1 -= 11;//a1 = a1 - 11

        b1 -= 1000; //b1 = b1 - 1000

        System.out.println(a1);

        System.out.println(b1);
    }
}
