package src.JavaBase;

public class Symbols {

    public static void main(String[] args) {

        char a = '\n';

        char b = '\t';

        System.out.println("SARATH" + a + "KUTTU");

        System.out.println("SARATH" + b + "KUTTU");

        System.out.println("SARATH\tKUTTU");
        System.out.println("SARATH\nKUTTU");

        System.out.println("SARATH\bKUTTU");

        System.out.println("SARATH\rKUTTU");
    }
}
