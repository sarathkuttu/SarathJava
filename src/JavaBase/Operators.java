package src.JavaBase;

public class Operators {

    public static void main(String[] args) {

        double a = 20;
        double b = 99;

        double sum = a + b;

        System.out.println(sum);

        double sub = a - b;

        System.out.println(sub);

        double mul = a * b;

        System.out.println(mul);

        double div = a / b;

        System.out.println(div);

        double mod = a % b;

        System.out.println(mod);

    }
}
